function stableImageHash(value: string) {
  let acc = 0;
  for (let index = 0; index < value.length; index += 1) {
    acc = (acc + value.charCodeAt(index) * (index + 17)) % 0xffffff;
  }
  return acc;
}

function placeholderImageDataUrl(prompt: string, modelName: string, width: number, height: number, seed: number) {
  const hash = stableImageHash(`${modelName}:${prompt}:${seed}`);
  const hueA = hash % 360;
  const hueB = (hash * 7) % 360;
  const accentX = 90 + (hash % 240);
  const accentY = 80 + ((hash >> 3) % 200);
  const promptLine = prompt.trim() || "Generated image preview";
  const safePrompt = promptLine.replace(/[&<>"]/g, (match) => ({
    "&": "&amp;",
    "<": "&lt;",
    ">": "&gt;",
    "\"": "&quot;",
  }[match] ?? match));
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${height}" viewBox="0 0 ${width} ${height}">
      <defs>
        <linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stop-color="hsl(${hueA} 72% 58%)" />
          <stop offset="100%" stop-color="hsl(${hueB} 68% 46%)" />
        </linearGradient>
      </defs>
      <rect width="${width}" height="${height}" rx="28" fill="url(#bg)" />
      <circle cx="${accentX}" cy="${accentY}" r="${Math.max(42, Math.round(width * 0.12))}" fill="rgba(255,255,255,0.18)" />
      <circle cx="${width - accentX}" cy="${height - accentY}" r="${Math.max(36, Math.round(width * 0.09))}" fill="rgba(8,12,20,0.18)" />
      <rect x="28" y="${height - 136}" width="${Math.max(240, width - 56)}" height="108" rx="24" fill="rgba(11,15,22,0.38)" stroke="rgba(255,255,255,0.14)" />
      <text x="52" y="${height - 90}" fill="white" font-size="28" font-family="SF Pro Display, Inter, sans-serif" font-weight="700">${modelName}</text>
      <text x="52" y="${height - 52}" fill="rgba(255,255,255,0.88)" font-size="19" font-family="SF Pro Text, Inter, sans-serif">${safePrompt.slice(0, 72)}</text>
    </svg>
  `.trim();
  return `data:image/svg+xml;charset=utf-8,${encodeURIComponent(svg)}`;
}

