export function createMockImageArtifact(
  payload: ImageGenerationPayload,
  modelName: string,
  index = 0,
): ImageOutputArtifact {
  const seedBase = payload.seed ?? stableImageHash(`${payload.modelId}:${payload.prompt}`);
  const seed = seedBase + index;
  const createdAt = new Date(Date.UTC(2026, 3, 9, 12, 0, Math.min(59, index * 7))).toISOString();
  return {
    artifactId: `mock-${stableImageHash(`${payload.modelId}:${payload.prompt}:${seed}:${index}`)}`,
    modelId: payload.modelId,
    modelName,
    prompt: payload.prompt,
    negativePrompt: payload.negativePrompt ?? "",
    width: payload.width,
    height: payload.height,
    steps: payload.steps,
    guidance: payload.guidance,
    seed,
    createdAt,
    durationSeconds: Number((1.8 + (payload.steps / 12) + index * 0.4).toFixed(1)),
    previewUrl: placeholderImageDataUrl(payload.prompt, modelName, payload.width, payload.height, seed),
    runtimeLabel: "Placeholder image engine",
  };
}

